// 5. Test Layout Functionality
function testLayoutFunctionality() {
    console.log('\nüéØ Testing Layout Functionality');
    console.log('===============================');
    
    if (!layoutManager || !currentNodes || currentNodes.length === 0) {
        console.log('‚ùå Prerequisites not met for layout testing');
        return;
    }
    
    console.log('üé® Testing circular layout...');
    
    const edgeData = currentEdges.map(edge => ({
        source: edge.startNode.id,
        target: edge.endNode.id
    }));
    
    layoutManager.applyLayout('circular', currentNodes, edgeData)
        .then(() => {
            console.log('‚úÖ Circular layout applied successfully!');
            console.log('üéâ All basic tests completed successfully!');
            console.log('\nüí° You can now run additional tests:');
            console.log('   - testAllLayouts()');
            console.log('   - testMemoryManagement()');
            console.log('   - testPerformanceOptimization()');
        })
        .catch((error) => {
            console.log(`‚ùå Layout test failed: ${error.message}`);
        });
}

// 6. Test All Layouts
function testAllLayouts() {
    console.log('\nüé® Testing All Layout Algorithms');
    console.log('================================');
    
    if (!layoutManager || !currentNodes || currentNodes.length === 0) {
        console.log('‚ùå Prerequisites not met');
        return;
    }
    
    const layouts = layoutManager.getAvailableLayouts();
    let currentIndex = 0;
    
    const testNextLayout = () => {
        if (currentIndex >= layouts.length) {
            console.log('‚úÖ All layouts tested successfully!');
            return;
        }
        
        const layoutName = layouts[currentIndex];
        console.log(`üéØ Testing ${layoutName} layout...`);
        
        const edgeData = currentEdges.map(edge => ({
            source: edge.startNode.id,
            target: edge.endNode.id
        }));
        
        layoutManager.applyLayout(layoutName, currentNodes, edgeData)
            .then(() => {
                console.log(`‚úÖ ${layoutName} layout successful`);
                currentIndex++;
                setTimeout(testNextLayout, 3000); // Wait 3 seconds between layouts
            })
            .catch((error) => {
                console.log(`‚ùå ${layoutName} layout failed: ${error.message}`);
                currentIndex++;
                setTimeout(testNextLayout, 1000);
            });
    };
    
    testNextLayout();
}

// 7. Memory Management Test
function testMemoryManagement() {
    console.log('\nüß† Testing Memory Management');
    console.log('============================');
    
    if (typeof Edge === 'undefined' || !Edge.geometryCache) {
        console.log('‚ùå Edge.geometryCache not available');
        return;
    }
    
    const initialSize = Edge.geometryCache.size;
    console.log(`üìä Initial cache size: ${initialSize}`);
    
    // Load different networks and check cache behavior
    const networks = ['smallData', 'mediumData', 'largeData'];
    let networkIndex = 0;
    
    const testNextNetwork = () => {
        if (networkIndex >= networks.length) {
            const finalSize = Edge.geometryCache.size;
            console.log(`üìä Final cache size: ${finalSize}`);
            
            if (finalSize <= initialSize + 10) {
                console.log('‚úÖ Memory management working correctly!');
            } else {
                console.log('‚ö†Ô∏è Cache size increased significantly');
            }
            return;
        }
        
        const networkButton = document.getElementById(networks[networkIndex]);
        if (networkButton) {
            console.log(`üîÑ Loading ${networks[networkIndex]}...`);
            networkButton.click();
            
            setTimeout(() => {
                console.log(`üìä Cache size after ${networks[networkIndex]}: ${Edge.geometryCache.size}`);
                networkIndex++;
                setTimeout(testNextNetwork, 2000);
            }, 2000);
        } else {
            console.log(`‚ùå Button ${networks[networkIndex]} not found`);
            networkIndex++;
            setTimeout(testNextNetwork, 500);
        }
    };
    
    testNextNetwork();
}

