// 3. Force Module Initialization
function forceModuleInitialization() {
    console.log('\nüîÑ Force Module Initialization');
    console.log('==============================');
    
    // Wait for main.js to load and initialize
    let attempts = 0;
    const maxAttempts = 10;
    
    const checkAndWait = () => {
        attempts++;
        console.log(`üîÑ Attempt ${attempts}/${maxAttempts} - Checking module initialization...`);
        
        if (typeof scene !== 'undefined' && typeof layoutManager !== 'undefined') {
            console.log('‚úÖ Modules successfully initialized!');
            runBasicTests();
        } else if (attempts < maxAttempts) {
            console.log('‚è≥ Modules not ready yet, waiting 2 seconds...');
            setTimeout(checkAndWait, 2000);
        } else {
            console.log('‚ùå Modules failed to initialize after maximum attempts');
            console.log('üí° Try refreshing the page or check for JavaScript errors');
        }
    };
    
    checkAndWait();
}

